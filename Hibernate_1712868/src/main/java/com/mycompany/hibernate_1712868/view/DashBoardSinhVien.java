    private void btnThayDoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThayDoiActionPerformed
        // TODO add your handling code here:
        String MSSV= Login.userName;
        String mkMoi= txtMKMoi.getText();
        String XacNhan= txtxacNhan.getText();
        if(!(MSSV.isEmpty()&mkMoi.isEmpty()))
        {
            Taikhoan tk=taiKhoanDAO.getTK(MSSV);
            if(mkMoi.equals(XacNhan))
            {
                tk.setMatKhau(mkMoi);
                 boolean flag=taiKhoanDAO.capNhatTK(tk);
                if(flag==true)
                    JOptionPane.showMessageDialog(this,"Đổi Mật Khẩu Thành Công");
                else
                    JOptionPane.showMessageDialog(this,"Đổi Mật Khẩu Thất Bại");
            }
            else
            JOptionPane.showMessageDialog(this,"Mật Khẩu Mới Và Xác Nhân Khác Nhau");
            
        } 
        else
            JOptionPane.showMessageDialog(this,"Bạn Chưa Nhâp Tài Khoản Để Reset");
        
    }//GEN-LAST:event_btnThayDoiActionPerformed

