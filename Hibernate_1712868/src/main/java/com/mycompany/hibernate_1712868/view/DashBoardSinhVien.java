    public void showDangKy(Set<Dangky> listDangKy)
    {
        String names[] = {"Mã Môn", "Tên Môn","Điểm GK","Điểm CK","Điểm Khác","Điểm Tổng"};
        
        DefaultTableModel dsModel=new DefaultTableModel(null,names);
        jTable1.setModel(dsModel);
        jTable1.setFocusable(true);
        
        bangDangKy bdk = new bangDangKy(listDangKy);
        //bsv.show();
        bdk.xuatDSDiemSinhVien(dsModel);
            
    }
